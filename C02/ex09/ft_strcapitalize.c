char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] += 32;
		}
		i++;
	}
	return (str);
}

char	*ft_strcapitalize(char *str)
{
	int		i;
	int		m;

	i = 0;
	m = 1;
	ft_strlowcase(str);
	while (str[i] != '\0')
	{
		if (str[i] >= 97 && str[i] <= 122)
		{
			if (m == 1)
				str[i] -= 32;
			m = 0;
		}
		else if (str[i] >= 48 && str[i] <= 57)
			m = 0;
		else
			m = 1;
		i++;
	}
	return (str);
}
